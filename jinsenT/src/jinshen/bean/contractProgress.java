package jinshen.bean;

public class contractProgress {
	private double totalStere;
	private double treenumber;
	private String percenttree;
	public double getTotalStere() {
		return totalStere;
	}
	public void setTotalStere(double totalStere) {
		this.totalStere = totalStere;
	}
	public double getTreenumber() {
		return treenumber;
	}
	public void setTreenumber(double treenumber) {
		this.treenumber = treenumber;
	}
	public String getPercenttree() {
		return percenttree;
	}
	public void setPercenttree(String percenttree) {
		this.percenttree = percenttree;
	}
	
	

}
