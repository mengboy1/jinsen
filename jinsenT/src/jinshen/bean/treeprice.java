package jinshen.bean;

public class treeprice {

	private String tname;
	private double treeradius;
	private double price;
	private double sesson;
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public double getTreeradius() {
		return treeradius;
	}
	public void setTreeradius(double treeradius) {
		this.treeradius = treeradius;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public double getSesson() {
		return sesson;
	}
	public void setSesson(double sesson) {
		this.sesson = sesson;
	}
	
}
